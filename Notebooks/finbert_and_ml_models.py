def run_pipeline(df):

    df = df.copy()
    df["date"] = pd.to_datetime(df["date"], errors="coerce")
    df = df.dropna(subset=["date"]).reset_index(drop=True)

    df["title"] = df["title"].fillna("")
    df["description"] = df["description"].fillna("")
    df["clean_text"] = (df["title"] + " " + df["description"]).apply(clean_text)

    df = ensure_datetime(df)

    fin = FinBert()
    df = fin.run(df)

    df = add_weekend(df)

    daily = create_daily_sentiment(df)

    start, end = df["date"].min(), df["date"].max()
    nifty = fetch_nifty(start, end)
    nifty_nz = nifty[nifty["label_"] != "zero"]

    merged = daily.merge(
        nifty_nz[["Date", "label_","return"]],
        left_on="date",
        right_on="Date",
        how="inner",
    )

    run_chi_square(merged)

    return df, daily, nifty, merged

