# DATETIME BUILDER
##############################################################
def ensure_datetime(df):

    # Convert date column safely
    df["date"] = pd.to_datetime(df["date"], errors="coerce")

    # FIX: Ensure a proper "time" column exists
    if "time" not in df.columns:
        df["time"] = "00:00:00"           # create full column
    else:
        df["time"] = df["time"].fillna("00:00:00")

    # Parse time safely
    df["_t"] = pd.to_datetime(df["time"], errors="coerce").dt.time
    df["_t"] = df["_t"].fillna(pd.to_datetime("00:00:00").time())

    # Build datetime
    df["datetime"] = df.apply(
        lambda r: pd.Timestamp.combine(r["date"], r["_t"])
        if pd.notna(r["date"]) else pd.NaT,
        axis=1
    )

    # Normalize date again
    df["date"] = df["datetime"].dt.date

    df.drop(columns=["_t"], inplace=True)
    return df



