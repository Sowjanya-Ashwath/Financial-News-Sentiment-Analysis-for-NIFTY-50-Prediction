def create_daily_sentiment(df):
    def bucket(x):
        if x > 0.2: return "positive"
        if x < -0.2: return "negative"
        return "neutral"

    df["sent_bucket"] = df[SENTIMENT_COL].apply(bucket)

    daily = (
        df.groupby("date").agg(
            daily_label=("sent_bucket", lambda x: x.mode()[0]),
            daily_finbert_score=(SENTIMENT_COL, "mean")  # ← add this
        )
        .reset_index()
    )
    return daily

