def fetch_nifty(start, end):
    print("Downloading NIFTY data…")
    nifty = yf.download("^NSEBANK", start=start, end=end)

    if isinstance(nifty.columns, pd.MultiIndex):
        nifty.columns = ["_".join(col).strip() for col in nifty.columns]

    nifty = nifty.reset_index()
    nifty["Date"] = pd.to_datetime(nifty["Date"]).dt.date
    close_col = [c for c in nifty.columns if "Close" in c][0]

    nifty["return"] = nifty[close_col].pct_change()

    def lab(x):
        if x > 0: return "positive"
        if x < 0: return "negative"
        return "zero"

    nifty["label_"] = nifty["return"].apply(lab)
    return nifty


