# CLEAN TEXT
##############################################################
def clean_text(t):
    if pd.isna(t): return ""
    t = re.sub(r"https?://\S+", "", str(t))
    t = re.sub(r"[^\w\s.,!'-]", " ", t)
    t = re.sub(r"\s+", " ", t)
    return t.strip()


