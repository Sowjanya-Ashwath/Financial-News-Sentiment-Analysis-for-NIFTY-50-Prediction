# CHI-SQUARE TEST
##############################################################
def run_chi_square(df):
    ct = pd.crosstab(df["daily_label"], df["label_"])
    print(ct)

    chi2, p, dof, exp = chi2_contingency(ct)

    print("\nChi2:", chi2)
    print("p-value:", p)
    print("dof:", dof)
    print("Expected:\n", exp)

    return chi2, p, dof, exp


