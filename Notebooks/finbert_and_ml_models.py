# WEEKEND EFFECT
##############################################################
def add_weekend(df):
    df["day_of_week"] = df["datetime"].dt.weekday
    df["time_only"] = df["datetime"].dt.time

    mo = pd.to_datetime(MARKET_OPEN_TIME).time()
    mc = pd.to_datetime(MARKET_CLOSE_TIME).time()

    def is_weekend(r):
        dow, t = r["day_of_week"], r["time_only"]
        if pd.isna(dow) or pd.isna(t): return 0
        if dow in (5, 6): return 1
        if dow == 4 and t >= mc: return 1
        if dow == 0 and t <= mo: return 1
        return 0

    df["is_weekend_article"] = df.apply(is_weekend, axis=1)

    def map_mon(dt):
        if pd.isna(dt): return pd.NaT
        dow, t = dt.weekday(), dt.time()
        if dow == 5: return (dt + pd.Timedelta(days=2)).date()
        if dow == 6: return (dt + pd.Timedelta(days=1)).date()
        if dow == 4 and t >= mc: return (dt + pd.Timedelta(days=3)).date()
        if dow == 0 and t <= mo: return dt.date()
        return pd.NaT

    df["mapped_monday"] = df.apply(
        lambda r: map_mon(r["datetime"]) if r["is_weekend_article"] else pd.NaT,
        axis=1,
    )

    weekend = (
        df[df["is_weekend_article"] == 1]
        .groupby("mapped_monday")[SENTIMENT_COL]
        .agg(["sum", "count", "mean"])
        .reset_index()
        .rename(columns={
            "mapped_monday": "date",
            "sum": "weekend_sentiment_score",
            "count": "weekend_article_count",
            "mean": "weekend_sentiment_avg"
        })
    )

    df["date"] = df["datetime"].dt.date
    df = df.merge(weekend, on="date", how="left")

    df["weekend_sentiment_score"] = df["weekend_sentiment_score"].fillna(0)
    df["weekend_article_count"] = df["weekend_article_count"].fillna(0)
    df["weekend_sentiment_avg"] = df["weekend_sentiment_avg"].fillna(0)

    return df


