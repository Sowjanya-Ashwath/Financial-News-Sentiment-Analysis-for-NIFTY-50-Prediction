# FINBERT (GPU AUTO)
##############################################################
class FinBert:
    def __init__(self):
        try:
            import torch
            device = 0 if torch.cuda.is_available() else -1
        except:
            device = -1

        self.pipe = pipeline(
            "sentiment-analysis",
            model=AutoModelForSequenceClassification.from_pretrained(FINBERT_MODEL),
            tokenizer=AutoTokenizer.from_pretrained(FINBERT_MODEL),
            device=device,
        )

        self.map = {"positive": 1, "negative": -1, "neutral": 0}

    def convert_score(self, label, conf):
        return self.map.get(label.lower(), 0) * conf

    def run(self, df):
        texts = df["clean_text"].tolist()
        labels, scores, confs = [], [], []

        for i in tqdm(range(0, len(texts), BATCH_SIZE), desc="FinBERT"):
            batch = texts[i:i+BATCH_SIZE]
            preds = self.pipe(batch, truncation=True)
            for p in preds:
                labels.append(p["label"])
                confs.append(p["score"])
                scores.append(self.convert_score(p["label"], p["score"]))

        df[LABEL_COL] = labels
        df[SENTIMENT_COL] = scores
        df["confidence"] = confs
        return df


